

export function getIcon(record_type){


    switch(record_type){

        case "Action": 
            return htmlIconCheckCircle()
        case "DeleteAction":
            return htmlIconXCircle()
        case "AddAction":
            return htmlIconPlusCircle()
        case "ReplaceAction":
            return htmlIconSave()
        case "InsertAction":
            return htmlIconPlusCircle()
        case "BookmarkAction":
            return htmlIconBookmarkOff()
        case "Person":
            return htmlIconPerson()
        case "Organization":
            return htmlIconOrganization()
        case "Product":
            return htmlIconBoxSeam()
        case "Event":
            return htmlIconCalendar()
        case "Brand":
            return htmlIconRCircle()
        case "Order":
            return htmlIconCart()
        case "Offer": 
            return htmlIconTag()
        case "service":
            return htmlIconPersonCheck()
        case "CreativeWork":
            return htmlIconContent()
        case "Article":
            return htmlIconContent()
        default:
            return htmlFileText()
            
    }

    
}





